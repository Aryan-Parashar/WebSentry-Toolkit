init(autoreset=True)

def find_input_fields(url):
    try:
        response = requests.get(url)
        soup = BeautifulSoup(response.text, 'html.parser')
        input_fields = soup.find_all('input')
        return [field.get('name') for field in input_fields]
    except Exception as e:
        print(f"Error occurred while fetching input fields from {url}: {e}")
        return []

def test_sql_injection(url, field_name, payload, payload_name):
    try:
        response = requests.post(url, data={field_name: payload})
        if "SQL" in response.text or "error" in response.text.lower() or "syntax" in response.text.lower():
            print(Fore.GREEN + f"SQL Injection vulnerability detected at: {url} for payload {payload_name} at input field {field_name}")
            return True
        else:
            print(Fore.YELLOW + f"No SQL Injection vulnerability detected at: {url} for payload {payload_name} at input field {field_name}")
            return False
    except requests.RequestException as e:
        print(Fore.RED + f"Error occurred while testing {url}: {e}")
        return False

